const makeOptions = (topic, language) => [
  `${topic} in ${language}`,
  `Incorrect use of ${topic}`,
  `${topic} as a database command`,
  `${topic} only for UI styling`
];

const makeQuestions = (language, topics) =>
  topics.map((topic, index) => ({
    id: `${language}-${index + 1}`,
    question: `${index + 1}. What best describes ${topic} in ${language}?`,
    options: makeOptions(topic, language),
    answerIndex: 0,
    explanation: `${topic} is a core concept in ${language} and is commonly tested in interviews and quizzes.`
  }));

